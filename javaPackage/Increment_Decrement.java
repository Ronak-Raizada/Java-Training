//Increment (++) - that means increase the value by 1

//Decrement (--)the value - that means decrease the value by 1

// with the help of integer value we get the incremented and decremented value in the console.

package javaPackage;

public class Increment_Decrement {

	public static void main(String[] args) {
		
		
		int a = 10;
		a++;
		
		System.out.println("Increemented value "+a);
		
		a--;
		
		System.out.println("Decremented value "+a);

	}

}
